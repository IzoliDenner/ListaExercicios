# Faça um Programa que calcule a área de um quadrado, em seguida mostre o dobro desta área para o usuário.

largura = float(input('Digite a largura do quadrado: '))
comprimento = float(input('Digite o comprimento do quadrado: '))

print(f'A área Total deste quadrado é de {largura * comprimento} e o dobro deste resultado é de: {(largura * comprimento) * 2}')

